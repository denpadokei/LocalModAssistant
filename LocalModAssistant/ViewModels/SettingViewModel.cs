using LocalModAssistant.Interfaces;
using Microsoft.WindowsAPICodePack.Dialogs;
using Prism.Commands;
using Prism.Mvvm;
using Prism.Services.Dialogs;
using System;
using System.Collections.Generic;
using System.Linq;
using Unity;

namespace LocalModAssistant.ViewModels
{
    public class SettingViewModel : BindableBase, IDialogAware
    {
        //ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*
        #region // プロパティ
        public string Title => "Setting";

        /// <summary>インストールフォルダ を取得、設定</summary>
        private string installDirectory_;
        /// <summary>インストールフォルダ を取得、設定</summary>
        public string InstallDirectory
        {
            get => this.installDirectory_;

            set => this.SetProperty(ref this.installDirectory_, value);
        }

        /// <summary>MOD置き場 を取得、設定</summary>
        private string ModDirectory_;
        /// <summary>MOD置き場 を取得、設定</summary>
        public string ModDirectory
        {
            get => this.ModDirectory_;

            set => this.SetProperty(ref this.ModDirectory_, value);
        }
        #endregion
        //ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*
        #region // コマンド
        public event Action<IDialogResult> RequestClose;
        #endregion
        //ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*
        #region // コマンド用メソッド
        private DelegateCommand _selectInstallFolder;
        public DelegateCommand SelectInstallFolder =>
            _selectInstallFolder ?? (_selectInstallFolder = new DelegateCommand(ExecuteSelectInstallFolder));

        void ExecuteSelectInstallFolder()
        {
            var dlg = new CommonOpenFileDialog()
            {
                IsFolderPicker = true
            };
            if (dlg.ShowDialog() != CommonFileDialogResult.Ok) {
                return;
            }
            this.InstallDirectory = dlg.FileName;
        }

        private DelegateCommand _selectModFolder;
        public DelegateCommand SelectModFolder =>
            _selectModFolder ?? (_selectModFolder = new DelegateCommand(ExecuteSelectModFolder));

        void ExecuteSelectModFolder()
        {
            var dlg = new CommonOpenFileDialog()
            {
                IsFolderPicker = true
            };
            if (dlg.ShowDialog() != CommonFileDialogResult.Ok) {
                return;
            }
            this.ModDirectory = dlg.FileName;
        }
        #endregion
        //ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*
        #region // オーバーライドメソッド
        #endregion
        //ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*
        #region // パブリックメソッド
        public bool CanCloseDialog()
        {
            return true;
        }

        public void OnDialogClosed()
        {
            this._setting.InstallDirectory = this.InstallDirectory;
            this._setting.ModDirectory = this.ModDirectory;
        }

        public void OnDialogOpened(IDialogParameters parameters)
        {
            this.InstallDirectory = this._setting.InstallDirectory;
            this.ModDirectory = this._setting.ModDirectory;
        }
        #endregion
        //ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*
        #region // プライベートメソッド
        #endregion
        //ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*
        #region // メンバ変数
        ISetting _setting;
        #endregion
        //ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*
        #region // 構築・破棄
        public SettingViewModel(ISetting setting)
        {
            this._setting = setting;
        }
        #endregion
    }
}
